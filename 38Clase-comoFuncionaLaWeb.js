/*
Hyper
Text
Transfer
Protocol

HTTP es la base cuando hablamos de cliente-servidor. 
Cliente entender como: computador, navegador, celular
Servidor: donde se esta hospedando todo el proyecto al que queremos tener acceso. 
La base de esto es que el cliente puede solicitar o enviar información al servidor y recibir una respuesta del servidor.

CÓMO FUNCINA HTTP:
esta base de cliente-servidor, casi siempre va conectado a una URL. Cuando entramos al navegador y ponemos por ejemplo: www.platzi.com
empezamos una petición http que llega directo al servidor donde platzi esta hospedado y le va a decir 'oye necesito esta información' 
para renderizarla al usuario en su navegador y que pueda iniciar una interacción. 
El error 404 generalmente es cuando hacemos una petición al servidor y este no encuentra nada.

HTTP tiene ciertos verbos que ayudan a generar el tipo de petición o acción que queremos hacer o realizar. 
get: es obtener información; ejemplo, cuando ponemos www.platzi.com y damos enter esto genera un get. (puedes mandarme esta información?)
post: toma la información y la comparte, como postear en facebook. 
patch y put: actualiza la información que ya existe. (como el editar algo que ya existe.)
delete: borrar la información
*/