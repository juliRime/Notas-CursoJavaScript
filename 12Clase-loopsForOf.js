/*For of: es un loop de iterción por cada elemento (for) se hace esta cosa (of) hasta que se cumpla el loop buscado.
          Se utiliza en objetos iterabes como los Arrays y Strings que terminan siendo una lista de [algo]
for (variable of objeto){     la (variable) o elemento de nuestra lista (objeto) iterable. 
código a ejecutar
}
*/

